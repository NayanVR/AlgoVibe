export function calculateMove(key: string, playerXPos: number, playerYPos: number) {

    switch (key) {
        case 'w':
            if (playerYPos > 0 && maze[playerYPos - 1][playerXPos] === 0) {
                playerYPos--;
            }
            break;
        case 's':
            if (playerYPos < maze[0].length - 1 && maze[playerYPos + 1][playerXPos] === 0) {
                playerYPos++;
            }
            break;
        case 'a':
            if (playerXPos > 0 && maze[playerYPos][playerXPos - 1] === 0) {
                playerXPos--;
            }
            break;
        case 'd':
            if (playerXPos < maze.length - 1 && maze[playerYPos][playerXPos + 1] === 0) {
                playerXPos++;
            }
            break;
    }
    return {
        x: playerXPos,
        y: playerYPos
    };
}